#include <iostream>
using namespace std;
// El objetivo será escribir un programa que imprima los números del 1 al 10 utilizando un ciclo

int main() {
    // Imprimir los números del 1 al 10 utilizando un ciclo
    for (int i = 1; i <= 10; ++i) {  
        cout << i << endl;  
    }

    

    return 0;
} 